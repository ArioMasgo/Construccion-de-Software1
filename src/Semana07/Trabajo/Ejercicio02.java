    double PromedioExame03() {
        double prom = 0;
        double suma= 0;
        for (int i = 0; i < estu.size(); i++) {
            suma += (double) estu.get(i).getNota3();
        }
        prom=suma/estu.size()+1;
        return prom ;
    }

    double PromedioMayor() {
        double prom = 0;
        for (int i = 0; i < estu.size(); i++) {
            for (int j = 0; j < estu.size(); j++) {
                if (estu.get(i).getPromedio() > estu.get(j).getPromedio()) {
                    prom = estu.get(i).getPromedio();
                }
            }
        }
        return prom;
    }

    int NumProvados() {
        int con = 0;
        for (int i = 0; i < estu.size(); i++) {
            if (estu.get(i).getPromedio() > 10) {
                con++;
            }
        }
        return con;
    }

    int NumDesaprovados() {
        int con = 0;
        for (int i = 0; i < estu.size(); i++) {
            if (estu.get(i).getPromedio() <= 10) {
                con++;
            }
        }
        return con;
    }

    int NumDesaprovadosExamen01() {
        int con = 0;
        for (int i = 0; i < estu.size(); i++) {
            if (estu.get(i).getNota1() <= 10) {
                con++;
            }
        }
        return con;
    }

